const FALLBACK_CODE = "FDNA-01";

export function resolveArchetypeBaseCode(baseCode) {
  if (baseCode && RESULT_EXTRA_SECTIONS[baseCode]) return baseCode;
  return FALLBACK_CODE;
}

export function getLocalizedResultExtraSections(baseCode, lang) {
  const code = resolveArchetypeBaseCode(baseCode);
  const data = RESULT_EXTRA_SECTIONS[code];
  const isZh = lang === "zh";

  const partnerNames = data.bestTeammate.partnerCodes.map((partnerCode) =>
    isZh
      ? ARCHETYPES[partnerCode]?.baseNameZh ?? partnerCode
      : ARCHETYPES[partnerCode]?.baseNameEn ?? partnerCode
  );

  return {
    baseCode: code,
    hero: {
      tagline: isZh ? data.tagline.zh : data.tagline.en,
      tags: isZh ? data.tags.zh : data.tags.en,
    },
    worldCupMoment: {
      title: isZh ? data.worldCupMoment.title : data.worldCupMoment.titleEn,
      titleEn: data.worldCupMoment.titleEn,
      content: isZh
        ? data.worldCupMoment.content
        : data.worldCupMoment.contentEn,
    },
    bestTeammate: {
      title: isZh ? data.bestTeammate.title : data.bestTeammate.titleEn,
      titleEn: data.bestTeammate.titleEn,
      partners: partnerNames,
      content: isZh
        ? data.bestTeammate.content
        : data.bestTeammate.contentEn,
    },
    nextUpgrade: {
      title: isZh ? data.nextUpgrade.title : data.nextUpgrade.titleEn,
      titleEn: data.nextUpgrade.titleEn,
      content: isZh ? data.nextUpgrade.content : data.nextUpgrade.contentEn,
    },
  };
}
